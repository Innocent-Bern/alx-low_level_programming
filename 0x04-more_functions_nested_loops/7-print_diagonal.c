#include "main.h"
/**
* print_diagonal - Entry point
* Description: prints diagonal line of length n
* @n: integer value
* Return: void
*/
void print_diagonal(int n)
{
	int i = 0, ii;

	while (i < n && n > 0)
	{
		ii = 0;
		while (ii < i)
		{
			_putchar(' ');
			ii++;
		}

		_putchar('\\');
		_putchar('\n');
		i++;
	}
	if (i == 0)
		_putchar('\n');
}